package pl.erizo.jcstress.playground.jcip.sequence;

public class SimplePojo {
    private int x;
    private int y;

    public SimplePojo(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
